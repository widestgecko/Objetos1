public class _p40Numeros100a1 {
    public static void main(String[] args) {
        //Variables
        int numero = 100;


        //Iniciamos el ciclo while
        while( numero >= 1 ) {
            System.out.print(numero+" ");
            numero-=1;
        }
        System.out.println("\nProceso terminado ...");
    }
}